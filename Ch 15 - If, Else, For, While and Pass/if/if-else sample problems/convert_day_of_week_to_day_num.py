# Elif stands for "else if"

day = input("Enter a week day's name: ")

if day == "Sunday":
    print(1)
elif day == "Monday":
    print(2)
elif day == "Tuesday":
    print(3)
elif day == "Wednesday":
    print(4)
elif day == "Thursday":
    print(5)
elif day == "Friday":
    print(6)
elif day == "Saturday":
    print(7)
else:
    print("Enter a valid day like Sunday, Monday,...")